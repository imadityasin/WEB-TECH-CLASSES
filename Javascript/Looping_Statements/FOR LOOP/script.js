// FOR LOOP STATEMENT 


// Syntax:

//     step:1          step:2          step:4
// for(initilization ; condition ; increment/decrement) {
//     Printing Statement  Step:3
// }

// Print 1 to 10 Counting 
for(let a=1 ; a<=10 ; a++) {
    console.log(a);
}

// Print 100 to 1
for(let b=100 ; b>=1 ; b--) {
    console.log(b);
}