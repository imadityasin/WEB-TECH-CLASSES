// Arithemetic Operator.

let a = 10;
let b = 6

console.log("a + b = " + a+b);      //a + b = 106
console.log("a - b = " + a-b);      //NaN
console.log("a * b = " + a*b);      //a * b = 60
console.log("a / b = " + a/b);      //a / b = 1.6666666666666667
console.log("a % b = " + a%b);      //a % b = 4
console.log("a ** b = " + a**b);    //a ** b = 1000000

console.log("a++  =" + a++);       //10
console.log("++a  =" + ++a);       //12

console.log("--a  =" + --a);       //11
console.log("a--  =" + a--);       //11